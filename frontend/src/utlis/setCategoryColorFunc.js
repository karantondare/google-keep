import React from "react";

export const setCategoryColorFunc = (bgColor, setCategoryColor) => {
  if (bgColor === "#FFFFFF") {
    setCategoryColor("#EBEBEB");
  } else if (bgColor === "#F28B82") {
    setCategoryColor("#DF8078");
  } else if (bgColor === "#FBBC04") {
    setCategoryColor("#E7AD04");
  } else if (bgColor === "#FFF475") {
    setCategoryColor("#EBE16C");
  } else if (bgColor === "#CCFF90") {
    setCategoryColor("#BCEB85");
  } else if (bgColor === "#A7FFEB") {
    setCategoryColor("#9AEBD9");
  } else if (bgColor === "#CBF0F8") {
    setCategoryColor("#BBDDE5");
  } else if (bgColor === "#AECBFA") {
    setCategoryColor("#A0BBE6");
  } else if (bgColor === "#D7AEFB") {
    setCategoryColor("#C6A0E7");
  } else if (bgColor === "#FDCFE8") {
    setCategoryColor("#E9BFD6");
  } else if (bgColor === "#E6C9A8") {
    setCategoryColor("#D4B99B");
  } else if (bgColor === "#E8EAED") {
    setCategoryColor("#D6D8DA");
  }
};
